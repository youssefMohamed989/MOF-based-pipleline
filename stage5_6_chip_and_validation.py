# ─────────────────────────────────────────────
# Langmuir Isotherm + Hill equation for sensor response
# ─────────────────────────────────────────────

def langmuir_response(C: np.ndarray, I_max: float, K_d: float) -> np.ndarray:
    """
    Langmuir adsorption isotherm:
        I(C) = I_max * C / (K_d + C)
    where:
        C     = analyte concentration (ng/mL)
        I_max = saturation current (µA)
        K_d   = dissociation constant (ng/mL)
    """
    return I_max * C / (K_d + C)


def hill_response(C: np.ndarray, I_max: float, K_d: float, n: float) -> np.ndarray:
    """
    Hill equation (generalised Langmuir with cooperativity exponent n).
    Used for LSPR shift prediction.
    """
    return I_max * C**n / (K_d**n + C**n)


def lod_loq(
    concentrations: np.ndarray,
    responses: np.ndarray,
    n_blank: int = 10,
    sigma_multiplier_lod: float = 3.0,
    sigma_multiplier_loq: float = 10.0,
) -> dict:
    """
    Calculates LOD and LOQ from blank signal noise and calibration slope.
      LOD = 3 * sigma_blank / slope
      LOQ = 10 * sigma_blank / slope
    """
    # Estimate blank noise from the lowest concentration region
    blank_noise = np.std(responses[:3]) * 0.15 + 0.02   # simulated blank σ
    # Linear slope at low concentrations
    low_mask = concentrations <= np.percentile(concentrations, 30)
    if low_mask.sum() >= 2:
        slope = np.polyfit(concentrations[low_mask], responses[low_mask], 1)[0]
    else:
        slope = (responses[1] - responses[0]) / (concentrations[1] - concentrations[0] + 1e-9)

    lod = sigma_multiplier_lod  * blank_noise / (slope + 1e-9)
    loq = sigma_multiplier_loq * blank_noise / (slope + 1e-9)
    return {"LOD_ng_mL": round(lod, 3), "LOQ_ng_mL": round(loq, 3),
            "blank_sigma": round(blank_noise, 4), "slope_uA_per_ngmL": round(slope, 4)}


# ─────────────────────────────────────────────
# Generate synthetic GPC3 calibration data
# (mimics experimental DPV measurements)
# ─────────────────────────────────────────────
def generate_calibration_data(
    concentrations: np.ndarray,
    I_max: float = 30.0,   # µA
    K_d: float = 8.5,      # ng/mL
    noise_std: float = 0.4,
    seed: int = 42,
) -> pd.DataFrame:
    rng = np.random.default_rng(seed)
    true_response = langmuir_response(concentrations, I_max, K_d)
    measured = true_response + rng.normal(0, noise_std, len(concentrations))
    measured = np.clip(measured, 0, None)
    return pd.DataFrame({
        "concentration_ng_mL": concentrations,
        "current_uA": measured.round(3),
        "true_current_uA": true_response.round(3),
    })


# ─────────────────────────────────────────────
# Generate LSPR shift data
# ─────────────────────────────────────────────
def generate_lspr_data(
    concentrations: np.ndarray,
    delta_max_nm: float = 18.0,
    K_d: float = 6.2,
    n_hill: float = 0.85,
    noise_std: float = 0.3,
    seed: int = 7,
) -> pd.DataFrame:
    rng = np.random.default_rng(seed)
    true_shift = hill_response(concentrations, delta_max_nm, K_d, n_hill)
    measured   = true_shift + rng.normal(0, noise_std, len(concentrations))
    measured   = np.clip(measured, 0, None)
    return pd.DataFrame({
        "concentration_ng_mL": concentrations,
        "lspr_shift_nm": measured.round(3),
        "true_shift_nm": true_shift.round(3),
    })


# ─────────────────────────────────────────────
# ROC Analysis — DEN-Induced Mouse Model
# ─────────────────────────────────────────────
def simulate_mouse_model(
    n_healthy: int = 40,
    n_tumor: int = 40,
    gpc3_healthy_mean: float = 0.8,    # ng/mL
    gpc3_healthy_std: float = 0.5,
    gpc3_tumor_mean: float = 18.0,     # ng/mL
    gpc3_tumor_std: float = 6.0,
    seed: int = 0,
) -> pd.DataFrame:
    """Simulates GPC3 serum levels in DEN mouse model."""
    rng = np.random.default_rng(seed)
    healthy_gpc3 = rng.normal(gpc3_healthy_mean, gpc3_healthy_std, n_healthy).clip(0)
    tumor_gpc3   = rng.normal(gpc3_tumor_mean,   gpc3_tumor_std,   n_tumor).clip(0)

    df = pd.DataFrame({
        "gpc3_ng_mL": np.concatenate([healthy_gpc3, tumor_gpc3]),
        "label":      np.concatenate([np.zeros(n_healthy), np.ones(n_tumor)]),
    })
    return df.sample(frac=1, random_state=seed).reset_index(drop=True)


def compute_roc_metrics(y_true: np.ndarray, y_scores: np.ndarray) -> dict:
    """Full ROC analysis: AUC, optimal threshold, sensitivity, specificity."""
    fpr, tpr, thresholds = roc_curve(y_true, y_scores)
    auc = roc_auc_score(y_true, y_scores)

    # Youden's J statistic for optimal threshold
    J = tpr - fpr
    opt_idx = np.argmax(J)
    opt_threshold = thresholds[opt_idx]
    y_pred = (y_scores >= opt_threshold).astype(int)
    tn, fp, fn, tp = confusion_matrix(y_true, y_pred).ravel()

    sensitivity = tp / (tp + fn)
    specificity = tn / (tn + fp)
    ppv = tp / (tp + fp) if (tp + fp) > 0 else 0
    npv = tn / (tn + fn) if (tn + fn) > 0 else 0

    return {
        "fpr": fpr, "tpr": tpr, "thresholds": thresholds,
        "auc": auc,
        "optimal_threshold_ng_mL": opt_threshold,
        "sensitivity": sensitivity,
        "specificity": specificity,
        "ppv": ppv,
        "npv": npv,
    }


