def _style_ax(ax):
    ax.tick_params(colors="#5a7a9a")
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)
    for sp in ["bottom", "left"]:
        ax.spines[sp].set_color("#1a2e4a")


