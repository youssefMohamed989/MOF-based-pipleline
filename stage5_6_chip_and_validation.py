class ChipDesign:
    """
    Stores and exports the geometry-programmed 3D-printed sensor chip.
    All dimensions in µm unless stated.
    """
    def __init__(
        self,
        electrode_pitch_um: float = 200.0,
        electrode_width_um: float = 80.0,
        electrode_length_mm: float = 8.0,
        n_electrodes: int = 16,
        layer_height_um: float = 20.0,
        n_layers: int = 3,
        feed_rate_mm_min: float = 200.0,
        extrusion_pressure_kPa: float = 35.0,
    ):
        self.pitch = electrode_pitch_um
        self.width = electrode_width_um
        self.length_mm = electrode_length_mm
        self.n_electrodes = n_electrodes
        self.layer_height = layer_height_um
        self.n_layers = n_layers
        self.feed_rate = feed_rate_mm_min
        self.pressure = extrusion_pressure_kPa

    def summary(self) -> pd.DataFrame:
        """Returns chip design summary as a DataFrame."""
        total_width_mm = (self.n_electrodes * self.pitch) / 1000
        return pd.DataFrame([{
            "electrode_pitch_um":   self.pitch,
            "electrode_width_um":   self.width,
            "electrode_length_mm":  self.length_mm,
            "n_electrodes":         self.n_electrodes,
            "layer_height_um":      self.layer_height,
            "n_layers":             self.n_layers,
            "chip_width_mm":        round(total_width_mm, 2),
            "chip_area_mm2":        round(total_width_mm * self.length_mm, 2),
            "feed_rate_mm_min":     self.feed_rate,
            "pressure_kPa":         self.pressure,
        }])

    def to_gcode(self, out_path: str):
        """Generates G-code for direct-ink-write 3D printing."""
        p_mm = self.pitch / 1000       # µm → mm
        w_mm = self.width / 1000
        lh_mm = self.layer_height / 1000

        lines = [
            "; AI-Accelerated MOF Sensor Chip — Direct-Ink-Write G-Code",
            "; Generated by stage5_chip_fabrication.py",
            f"; Electrodes: {self.n_electrodes}, Pitch: {self.pitch} µm",
            "G21          ; Units: mm",
            "G90          ; Absolute positioning",
            "G28          ; Home all axes",
            f"G1 F{self.feed_rate}   ; Set feed rate",
            "",
        ]

        for layer in range(self.n_layers):
            z = (layer + 1) * lh_mm
            lines.append(f"; === Layer {layer+1} (Z={z:.3f} mm) ===")
            lines.append(f"G1 Z{z:.3f}")

            for i in range(self.n_electrodes):
                x_start = round(i * p_mm, 3)
                x_end   = round(x_start + w_mm, 3)
                # Travel to start (nozzle up)
                lines.append(f"G1 X{x_start} Y0 Z{z+0.1:.3f}  ; travel")
                lines.append(f"G1 Z{z:.3f}                    ; lower")
                # Print electrode line
                lines.append(f"G1 X{x_start} Y{self.length_mm}  ; electrode {i+1}")
                # Lift
                lines.append(f"G1 Z{z+0.1:.3f}               ; lift")

        lines += ["", "G28 Z   ; raise Z", "M84     ; motors off", "; END"]

        with open(out_path, "w") as f:
            f.write("\n".join(lines))
        print(f"  ✓ G-code written: {out_path}  ({len(lines)} lines)")


